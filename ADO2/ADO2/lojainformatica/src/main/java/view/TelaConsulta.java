    public void atualizarTabela(){
        ArrayList<Computador> lista = ComputadorDAO.listar();
        DefaultTableModel tabela = (DefaultTableModel) tblComputador.getModel();
        
        tabela.setRowCount(0);
        
        for(Computador computador : lista){
            tabela.addRow(new String[]{
                String.valueOf(computador.getId()),
                computador.getProcessador(),
                computador.getHD(),
                computador.getMarca()
            });
            
            
        }
    }

