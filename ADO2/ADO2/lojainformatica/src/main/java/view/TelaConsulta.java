    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int linhaSelecionada = tblComputador.getSelectedRow();
        
        if(linhaSelecionada >=0){
            DefaultTableModel tabela = (DefaultTableModel)tblComputador.getModel();
            int idExcluir = Integer.parseInt(tabela.getValueAt(linhaSelecionada, 0).toString());
            
            
            boolean retorno = ComputadorDAO.excluir(idExcluir);
            
            if(retorno){
                JOptionPane.showMessageDialog(rootPane, "Exclusão feita!");
                atualizarTabela();
            }else{
                JOptionPane.showMessageDialog(rootPane, "Falha na exclusão");
            }
        }else{
            JOptionPane.showMessageDialog(rootPane, "Selecione uma linha");
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

