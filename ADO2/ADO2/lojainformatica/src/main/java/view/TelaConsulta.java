    private void btnProcurarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProcurarActionPerformed
        String filtro = txtFiltro.getText();
        
        if(filtro.equals("")){
            ArrayList<Computador> lista = ComputadorDAO.listar();
            DefaultTableModel tabela = (DefaultTableModel) tblComputador.getModel();

            tabela.setRowCount(0);

            for(Computador computador : lista){
                    tabela.addRow(new String[]{
                    String.valueOf(computador.getId()),
                    computador.getProcessador(),
                    computador.getHD(),
                    computador.getMarca()
                });
            
            
            }
        }else{
            ArrayList<Computador> lista = ComputadorDAO.listarFiltro(filtro);
            DefaultTableModel tabela = (DefaultTableModel) tblComputador.getModel();

            tabela.setRowCount(0);

            for(Computador computador : lista){
                    tabela.addRow(new String[]{
                    String.valueOf(computador.getId()),
                    computador.getProcessador(),
                    computador.getHD(),
                    computador.getMarca()
                });
            }
        }
        
        
    }//GEN-LAST:event_btnProcurarActionPerformed

    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        String alertaLinhas = "Selecione uma linha primeiro!";
        DefaultTableModel tabelaComputador = (DefaultTableModel) tblComputador.getModel();
        
        int linhasSelecionadas = tblComputador.getSelectedRow();
        
        if(linhasSelecionadas >= 0){
            int id = Integer.parseInt(tabelaComputador.getValueAt(linhasSelecionadas, 0).toString());
            String processador = tabelaComputador.getValueAt(linhasSelecionadas, 1).toString();
            String hd = tabelaComputador.getValueAt(linhasSelecionadas, 2).toString();
            
            objAlterar = new Computador(hd, processador);
            
            TelaCadastro cadastro = new TelaCadastro(this, objAlterar);
            cadastro.setVisible(true);
           
        }else{
            JOptionPane.showMessageDialog(rootPane, alertaLinhas);
        }
        
    }//GEN-LAST:event_btnAlterarActionPerformed

